/**
 **_strncpy - it will copy the string
 *@dest: the dest. The string will be copied to
 *@src: the string source
 *@n: the amounts of the chars being copied
 *Return: the string
 */
char *_strncpy(char *dest, char *src, int n)
{
	int a, b;
	char *s = dest;

	a = 0;
	while (src[a] != '\0' && a < n - 1)
	{
		dest[a] = src[a];
		a++;
	}
	if (a < n)
	{
		b = a;
		while (b < n)
		{
			dest[b] = '\0';
			b++;
		}
	}
	return (s);
}

